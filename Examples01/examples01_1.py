# 变量赋值
number = 10
name = "Alice"
is_student = True

# 打印变量
print(number)      # 输出: 10
print(name)        # 输出: Alice
print(is_student)  # 输出: True

# 整数类型
age = 25
print(age)  # 输出: 25

#数据类型例子

# 浮点数类型
height = 175.5
print(height)  # 输出: 175.5

# 字符串类型
greeting = "Hello, world!"
print(greeting)  # 输出: Hello, world!

# 布尔类型
is_tall = False
print(is_tall)  # 输出: False